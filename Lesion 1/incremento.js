let a = 3, b = 2
let c = a + b

//Incremento
//Pre-incremento (el operador ++ antes de la variable)
c = ++a
console.log(a)
console.log(c)

//Post-incremento (el operador ++ despues de la variable)
c = b++
console.log(b)
console.log(c)


//Decremento
//Pre-decremento
c = --a
console.log(a)
console.log(c)

//Post-decremento
c = b--
console.log(b)
console.log(c)