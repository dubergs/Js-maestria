let a = 1, b = 9

//Sumamos dos numeros
suma = a + b
console.log("Resultado Su: " + suma)

//Restamos dos numeros
resta = a - b
console.log("Resultado Re: " + resta)

//Multiplicamos dos numeros
multiplicacion = a * b
console.log("Resultado Mu: " + multiplicacion)

//Dividimos dos numeros
division = a / b
console.log("Resultado Di: " + division)

//Residuo de la division
residuo = a % b
console.log("Resultado Re: " + residuo)